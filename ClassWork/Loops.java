// import java.util.*;
public class Loops {
    public static void main(String[] args) {
        // Scanner sc=new Scanner(System.in);
        int i=1;
        while (i<=10) {
            System.out.println(i);
            i++;
        }
        for(i=1;i<=10;i++)
        {
            System.out.println(i);
        }
    }
}
